
public class TrocaDeValores {

	public static void main(String[] args) {
		
		int A = 1;
		int B = 2;
		int C = 3;
		
		System.out.println("A: " + A);
		System.out.println("B: " + B);
		System.out.println("C: " + C);

		System.out.println("========================");
		
		A = 3;
		B = 1;
		C = 2;
		
		System.out.println("A: " + A);
		System.out.println("B: " + B);
		System.out.println("C: " + C);

	}

}
